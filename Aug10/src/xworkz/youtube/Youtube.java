package xworkz.youtube;

public class Youtube {
	

		private String developCompany;
		private int startedYear;
		private float latestVersion;
		private String channelName;
		private String username;
		private String email;
		private int subscribersCount;
		private int videosUploaded;
		private int viewsCount;
		private String country;
		private String website;
		private boolean isVerified;
		private String about;
		private String joinDate;
		
		public String getDevelopCompany() {
			return this.developCompany;
		}

		public void setDevelopCompany(String developCompany) {
			this.developCompany = developCompany;
		}

		public int getStartedYear() {
			return this.startedYear;
		}

		public void setStartedYear(int startedYear) {
			this.startedYear = startedYear;
		}

		public float getLatestVersion() {
			return this.latestVersion;
		}

		public void setLatestVersion(float latestVersion) {
			this.latestVersion = latestVersion;
		}
		

		public String getChannelName() {
			return channelName;
		}

		public void setChannelName(String channelName) {
			this.channelName = channelName;
		}

		public String getUsername() {
			return username;
		}

		public void setUsername(String username) {
			this.username = username;
		}

		public String getEmail() {
			return email;
		}

		public void setEmail(String email) {
			this.email = email;
		}

		public int getSubscribersCount() {
			return subscribersCount;
		}

		public void setSubscribersCount(int subscribersCount) {
			this.subscribersCount = subscribersCount;
		}

		public int getVideosUploaded() {
			return videosUploaded;
		}

		public void setVideosUploaded(int videosUploaded) {
			this.videosUploaded = videosUploaded;
		}

		public int getViewsCount() {
			return viewsCount;
		}

		public void setViewsCount(int viewsCount) {
			this.viewsCount = viewsCount;
		}

		public String getCountry() {
			return country;
		}

		public void setCountry(String country) {
			this.country = country;
		}

		public String getWebsite() {
			return website;
		}

		public void setWebsite(String website) {
			this.website = website;
		}

		public boolean isVerified() {
			return isVerified;
		}

		public void setVerified(boolean verified) {
			isVerified = verified;
		}

		public String getAbout() {
			return about;
		}

		public void setAbout(String about) {
			this.about = about;
		}


		public String getJoinDate() {
			return joinDate;
		}

		public void setJoinDate(String joinDate) {
			this.joinDate = joinDate;
		}


	}



